/**
 * Anthropic Claude API helper for the Doctor Farmer MagicScript Pipeline.
 * High-EQ copywriting stages (6, 9) → claude-sonnet-4-6 (default)
 *
 * Claude is preferred over Gemini for tasks that require:
 * - Warm, conversational spoken-word dialogue (Stage 6)
 * - Complex two-column production script formatting (Stage 8)
 * - Social media hook writing and viral moment extraction (Stage 9)
 *
 * Gemini remains primary for pure data / logic tasks (Stage 7).
 */

export const CLAUDE_MODELS = {
  sonnet: "claude-sonnet-4-6",  // Claude Sonnet 4.6 — faster and cheaper than Opus
};

